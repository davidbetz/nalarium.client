//- encode -//
Nalarium.Base64.encode = function (input) {
   var output = "";
   var chr1, chr2, chr3;
   var enc1, enc2, enc3, enc4;
   var i = 0;

   do {
      chr1 = input.charCodeAt(i++);
      chr2 = input.charCodeAt(i++);
      chr3 = input.charCodeAt(i++);

      enc1 = chr1 >> 2;
      enc2 = ((chr1 & 3) << 4) | (chr2 >> 4);
      enc3 = ((chr2 & 15) << 2) | (chr3 >> 6);
      enc4 = chr3 & 63;

      if (isNaN(chr2)) {
         enc3 = enc4 = 64;
      } else if (isNaN(chr3)) {
         enc4 = 64;
      }

      output = output + Nalarium.Base64._keyString.charAt(enc1) + Nalarium.Base64._keyString.charAt(enc2) + 
         Nalarium.Base64._keyString.charAt(enc3) + Nalarium.Base64._keyString.charAt(enc4);
   } while (i < input.length);
//   alert(output);
   return output;
};

//- decode -//
Nalarium.Base64.decode = function (input) {
   var output = "";
   var chr1, chr2, chr3;
   var enc1, enc2, enc3, enc4;
   var i = 0;

   // remove all characters that are not A-Z, a-z, 0-9, +, /, or =
   input = input.replace(/[^A-Za-z0-9\+\/\=]/g, "");

   do {
      enc1 = Nalarium.Base64._keyString.indexOf(input.charAt(i++));
      enc2 = Nalarium.Base64._keyString.indexOf(input.charAt(i++));
      enc3 = Nalarium.Base64._keyString.indexOf(input.charAt(i++));
      enc4 = Nalarium.Base64._keyString.indexOf(input.charAt(i++));

      chr1 = (enc1 << 2) | (enc2 >> 4);
      chr2 = ((enc2 & 15) << 4) | (enc3 >> 2);
      chr3 = ((enc3 & 3) << 6) | enc4;

      output = output + String.fromCharCode(chr1);

      if (enc3 != 64) {
         output = output + String.fromCharCode(chr2);
      }
      if (enc4 != 64) {
         output = output + String.fromCharCode(chr3);
      }
   } while (i < input.length);

   return output;
};